#Codigo que pida un número del 1 al 7 y diga el día de la semana correspondiente
num = int(input("Digite un numero de 1 al 7: "))
if num == 1:
    print("Lunes")
if num == 2:
    print("Martes")
if num == 3:
    print("Miercoles")
if num == 4:
    print("Jueves")
if num == 5:
    print("Viernes")
if num == 6:
    print("Sabado")
if num == 7:
    print("Domingo")